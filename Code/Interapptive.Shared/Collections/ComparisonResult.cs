namespace Interapptive.Shared.Collections
{
    /// <summary>
    /// Result of a comparison
    /// </summary>
    public enum ComparisonResult
    {
        /// <summary>
        /// The left side has less than the right
        /// </summary>
        Less,

        /// <summary>
        /// Both sides are equal
        /// </summary>
        Equal,

        /// <summary>
        /// The left side has more than the right
        /// </summary>
        More
    }
}
