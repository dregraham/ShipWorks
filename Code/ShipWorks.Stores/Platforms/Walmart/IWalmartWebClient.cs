using System;
using ShipWorks.Data.Model.EntityInterfaces;
using ShipWorks.Stores.Platforms.Walmart.DTO;

namespace ShipWorks.Stores.Platforms.Walmart
{
    /// <summary>
    /// Web client for interacting with Walmart
    /// </summary>
    public interface IWalmartWebClient
    {
        /// <summary>
        /// Tests the connection to Walmart
        /// </summary>
        void TestConnection(IWalmartStoreEntity store);

        /// <summary>
        /// Get orders created after the given start date
        /// </summary>
        ordersListType GetOrders(IWalmartStoreEntity store, DateTime start);

        /// <summary>
        /// Get orders created using the next cursor token generated by walmart
        /// </summary>
        ordersListType GetOrders(IWalmartStoreEntity store, string nextCursor);

        /// <summary>
        /// Uploads the shipment details.
        /// </summary>
        Order UpdateShipmentDetails(IWalmartStoreEntity store, orderShipment shipment, string purchaseOrderID);

        /// <summary>
        /// Get the order with the given purchaseOrderId
        /// </summary>
        Order GetOrder(IWalmartStoreEntity store, string purchaseOrderId);
    }
}